static void AAS_AddBrightness(int add) {
  int i, r, g, b, val, t;
  AAS_u16 *dest = (AAS_u16 *)0x5000000;
  const AAS_u16 *palette_pos = AAS_Logo_Palette;

  for (i = 256; i > 0; --i) {
    val = *palette_pos++;
    r = AAS_Min((val >> 10) + add, 31);
    g = AAS_Min(((val >> 5) & 0x1f) + add, 31);
    b = AAS_Min((val & 0x1f) + add, 31);
    *dest++ = r + (g << 5) + (b << 10);
  }
}

static void AAS_AddBrightness2(int add) {
  int i, r, g, b, val, t;
  AAS_u16 *dest = (AAS_u16 *)0x5000000;
  const AAS_u16 *palette_pos = AAS_Logo_Palette;

  ++palette_pos;
  ++dest;

  for (i = 255; i > 0; --i) {
    val = *palette_pos++;
    r = AAS_Min((val >> 10) + add, 31);
    g = AAS_Min(((val >> 5) & 0x1f) + add, 31);
    b = AAS_Min((val & 0x1f) + add, 31);
    *dest++ = r + (g << 5) + (b << 10);
  }
}

static void AAS_SetBrightness(int brightness) {
  int i, r, g, b, val, t;
  AAS_u16 *dest = (AAS_u16 *)0x5000000;
  const AAS_u16 *palette_pos = AAS_Logo_Palette;

  for (i = 256; i > 0; --i) {
    val = *palette_pos++;
    r = ((val >> 10) * brightness) >> 5;
    g = (((val >> 5) & 0x1f) * brightness) >> 5;
    b = ((val & 0x1f) * brightness) >> 5;
    *dest++ = r + (g << 5) + (b << 10);
  }
}

static void AAS_BlankFrame(AAS_u32 val) {
  AAS_u32 *dest_pos = (AAS_u32 *)0x06000000;
  int i;

  for (i = 9600; i > 0; --i)
    *dest_pos++ = val;
}

static void AAS_CopyFrame() {
  const AAS_u32 *source_pos = AAS_Logo_Image;
  AAS_u32 *dest_pos = (AAS_u32 *)(0x06000000 + (240 * 51));
  int i;

  for (i = 2880; i > 0; --i)
    *dest_pos++ = *source_pos++;
}

static void AAS_WaitVBlank() {
  while (REG_VCOUNT >= 160)
    ;
  while (REG_VCOUNT < 160)
    ;
}

void AAS_ShowLogo() {
  int i, tmpx, tmpy;

  AAS_WaitVBlank();
  AAS_AddBrightness(32);
  DISPCNT = 0x0444;
  AAS_BlankFrame(0xfdfdfdfd);
  AAS_CopyFrame();

  for (i = 31; i >= 0; --i) {
    AAS_WaitVBlank();
    AAS_AddBrightness(i);
  }

  for (i = 120; i > 0; --i) {
    AAS_WaitVBlank();
  }

  for (i = 0; i <= 15; ++i) {
    AAS_WaitVBlank();

    if (i > 0) {
      tmpy = i << 10;
      REG_BG2PD = tmpy;
      REG_BG2Y = 20480 - (80 * tmpy);
    }

    AAS_AddBrightness2(i);
  }

  for (i = 16; i <= 31; ++i) {
    AAS_WaitVBlank();

    tmpx = (i - 16) << 11;
    tmpy = i << 10;
    REG_BG2PA = tmpx;
    REG_BG2PD = tmpy;
    REG_BG2X = 30720 - (120 * tmpx);
    REG_BG2Y = 20480 - (80 * tmpy);

    AAS_AddBrightness2(i);
  }

  for (i = 31; i >= 0; --i) {
    AAS_SetBrightness(i);
    AAS_WaitVBlank();
  }

  AAS_BlankFrame(0);

  REG_BG2PA = 256;
  REG_BG2PD = 256;
  REG_BG2X = 30720 - (120 * 256);
  REG_BG2Y = 20480 - (80 * 256);
}
